package session3;

public class StringExample {
	public static void main(String[] args) {
		String s1 = "sherin";// string literal
		String s2 = new String("sebastin");// string object
		char[] c1 = {'l','o','v','e'};//array of characters
		String s3 = new String(c1);//char to string 
		
		System.out.println(s1);	
		System.out.println(s2);
		System.out.println(s3);
	}

}
